char	*strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (*s)
	{
		s++;
		i++;
	}
	s--;
	while (i >= 0)
	{
		if (c == *s)
			return (s);
		i--;
		s--;
	}
	return (0);
}
