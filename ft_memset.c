#include <stdlib.h>

void *ft_memset(void *s, int c, size_t n)
{
	unsigned int	i;

	i = 0;
	while (i < n)
	{
		*(int *)s = c;
		s++;
		i++;
	}
	while (i != 0)
	{
		s--;
		i--;
	}
	return (s);
}
